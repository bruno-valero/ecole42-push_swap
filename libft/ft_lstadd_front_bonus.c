#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*sup;

	if (!lst || !new)
		return ;
	if (!lst[0])
		lst[0] = new;
	else
	{
		sup = lst[0];
		lst[0] = new;
		new->next = sup;
	}
}
