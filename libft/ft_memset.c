#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char			number;
	size_t					i;
	unsigned char			*ptr;

	number = (unsigned char)c;
	ptr = (unsigned char *)s;
	i = 0;
	while (i < n)
		ptr[i++] = number;
	return (s);
}
